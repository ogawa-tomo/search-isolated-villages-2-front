export const facultyNames = (facultyName: string) => {
  if (facultyName === 'post_office') return '郵便局';
  if (facultyName === 'elementary_school') return '小学校';
  if (facultyName === 'station') return '駅';
  if (facultyName === 'abandoned_station') return '廃駅';
  if (facultyName === 'research_institute') return '研究機関';
  if (facultyName === 'hot_spring') return '温泉';
  if (facultyName === 'new_town') return 'ニュータウン';
  if (facultyName === 'michinoeki') return '道の駅';
};
